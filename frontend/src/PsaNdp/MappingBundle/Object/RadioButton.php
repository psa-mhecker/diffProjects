<?php

namespace PsaNdp\MappingBundle\Object;

/**
 * Class RadioButton
 */
class RadioButton extends AbstractObject
{
    protected $mapping = array();

    /**
     * @var string $label
     */
    protected $label;

    /**
     * @var string $id
     */
    protected $id;

    /**
     * @return string
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param string $id
     *
     * @return $this
     */
    public function setId($id)
    {
        $this->id = $id;

        return $this;
    }

    /**
     * @return string
     */
    public function getLabel()
    {
        return $this->label;
    }

    /**
     * @param string $label
     *
     * @return $this
     */
    public function setLabel($label)
    {
        $this->label = $label;

        return $this;
    }
}
