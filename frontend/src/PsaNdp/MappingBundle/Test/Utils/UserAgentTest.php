    /**
     * Set up the test
     */
    public function setUp()
    {
        $file = realpath(__DIR__.'/../../../../../../backend/application/configs/Wurfl/1.5/wurfl-config.php');

        if (file_exists($file)) {
            $this->requestStack = new RequestStack();
            $firstRequest = Request::create('/bar');
            $this->requestStack->push($firstRequest);
            $this->config['config_file'] = $file;
            $this->cacheService = Phake::mock('PsaNdp\CacheBundle\Services\RedisService');

            //Phake::when($this->request)->getMasterRequest()->thenReturn($this->request);
            //Phake::when($this->requestStack)->getMasterRequest()->thenReturn($this->request);
            //Phake::when($this->cacheService)->getVariable(Phake::anyParameters())->thenReturn(null);
            Phake::when($this->cacheService)->setVariable(Phake::anyParameters())->thenReturn(true);

            $this->userAgent = new UserAgent($this->requestStack, $this->config, $this->cacheService);
        }
    }

    /**
     * @param $userAgent
     * @dataProvider dataIsMobile
     */
    public function testIsMobile($userAgent) {
        $this->userAgent->setServerParameter('HTTP_USER_AGENT', $userAgent);
        $this->assertTrue($this->userAgent->isMobile());
    }

    /**
     * @param $userAgent
     * @dataProvider dataIsNotMobile
     */
    public function testIsNotMobile($userAgent) {
        $this->userAgent->setServerParameter('HTTP_USER_AGENT', $userAgent);
        $this->assertTrue($this->userAgent->isMobileCompatible());
        $this->assertFalse($this->userAgent->isMobile());
    }

