    public function testMatchMobileOtherHeaders()
    {

        $this->userAgent->setServerParameter('HTTP_USER_AGENT', 'xxxxx');

        $this->userAgent->setServerParameter('ALL_HTTP', '. opera Mini');
        $this->assertTrue($this->userAgent->isMobileCompatible());
        $this->userAgent->setServerParameter('ALL_HTTP', '');

        $this->userAgent->setServerParameter('HTTP_X_WAP_PROFILE',
            'http://device.sprintpcs.com/Sanyo/PL3100/1003QW.rdf');
        $this->assertTrue($this->userAgent->isMobileCompatible());
        $this->userAgent->setServerParameter('HTTP_X_WAP_PROFILE', '');

        $this->userAgent->setServerParameter('HTTP_PROFILE', 'xx');
        $this->assertTrue($this->userAgent->isMobileCompatible());
        $this->userAgent->setServerParameter('HTTP_PROFILE', '');

        $this->userAgent->setServerParameter('HTTP_ACCEPT', 'midp');
        $this->assertTrue($this->userAgent->isMobileCompatible());

        $this->userAgent->setServerParameter('HTTP_ACCEPT',
            'text/html, image/vnd.wap.wbmp, image/png, image/jpeg, image/gif, image/bmp, application/vnd.wap.wmlc, application/vnd.wap.xhtml+xml, application/xhtml+xml, application/vnd.wap.multipart.mixed, multipart/mixed, text/vnd.wap.wml, application/vnd.oma.dd+xml, text/vnd.sun.j2me.app-descriptor, application/java-archive, *,text/x-hdml,image/mng,image/x-mng,video/mng,video/x-mng,image/bmp,text/html');
