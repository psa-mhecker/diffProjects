<?php

namespace PsaNdp\ApiBundle\Object;

/**
 * Class Color
 */
class Color
{
    /**
     * @var string $code
     */
    protected $code;

    /**
     * @var string $url
     */
    protected $url;

    /**
     * @var string $label
     */
    protected $label;

    /**
     * @return string
     */
    public function getCode()
    {
        return $this->code;
    }

    /**
     * @param string $code
     */
    public function setCode($code)
    {
        $this->code = $code;
    }

    /**
     * @return string
     */
    public function getUrl()
    {
        return $this->url;
    }

    /**
     * @param string $url
     */
    public function setUrl($url)
    {
        $this->url = $url;
    }

    /**
     * @return string
     */
    public function getLabel()
    {
        return $this->label;
    }

    /**
     * @param string $label
     */
    public function setLabel($label)
    {
        $this->label = $label;
    }
}
